    private void cbTimItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbTimItemStateChanged
        // TODO add your handling code here:
        tmDS.setRowCount(0);
        if(cbTim.getSelectedIndex()==1){
            List<QLSach>lsearch = new ArrayList<>();
            String key = jTextField9.getText();
            for(QLSach i:listDS){
                if(i.getS().getTen().toLowerCase().contains(key)){
                    lsearch.add(i);
                }
            }
            for(QLSach i:lsearch){
                tmDS.addRow(i.toObject());
            }
        }
        if(cbTim.getSelectedIndex()==2){
            List<QLSach>lsearch = new ArrayList<>();
            String key = jTextField9.getText();
            for(QLSach i:listDS){
                if(i.getS().getTacGia().toLowerCase().contains(key)){
                    lsearch.add(i);
                }
            }
            for(QLSach i:lsearch){
                tmDS.addRow(i.toObject());
            }
        }
    }//GEN-LAST:event_cbTimItemStateChanged

