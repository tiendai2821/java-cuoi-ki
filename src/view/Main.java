    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = tableKS.getSelectedRow();
        if(row>=0&&row<tableKS.getRowCount()){
            String ten,loai;
            int sl;
            try{
                ten = jTextField5.getText();
                sl = Integer.parseInt(jTextField7.getText());
                loai = cbLoai.getSelectedItem().toString();
                if(ten.isEmpty()) throw new TrongException();
                tmKS.setValueAt(ten, row, 1);
                tmKS.setValueAt(loai, row, 2);
                tmKS.setValueAt(sl, row, 3);
                int ma = Integer.parseInt(jTextField6.getText());
                KeSach sv = new KeSach(ma,ten,loai,sl);
                listKS.set(row, sv);
            } catch(TrongException e){
                JOptionPane.showMessageDialog(this,"Khong dc de trong");
                return;
            } catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this,"Phai nhap so");
                return;
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

